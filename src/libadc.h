#ifndef _LIBADC_H_
#define _LIBADC_H_


/*================================================================================================
マクロ定義
=================================================================================================*/
#define ADC_LOWMEM

#define ADC_MAX		3


/*================================================================================================
構造体
=================================================================================================*/
typedef struct ADC {
	int adc[ ADC_MAX];							/* PIC内蔵 ADC */

#ifndef ADC_LOWMEM
	unsigned int last;							/* 最後に実行した時刻 */
	unsigned int cycle;							/* 実行周期 */
#endif

} ADC_T;
typedef ADC_T* pADC_T;


/*=================================================================================================
グローバル変数
=================================================================================================*/
extern ADC_T adc;


/*================================================================================================
プロトタイプ宣言
=================================================================================================*/
extern void ADC_init(void);


#endif
